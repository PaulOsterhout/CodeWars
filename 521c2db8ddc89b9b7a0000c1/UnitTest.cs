		[Test]
		public void ChangeDirections1()
		{
			int xDir = 1, yDir = 0;

			void Change()
			{
				int temp = xDir;
				xDir = yDir * -1;
				yDir = temp * 1;
			}

			Assert.AreEqual(1, xDir);
			Assert.AreEqual(0, yDir);
			Change();
			Assert.AreEqual(0, xDir);
			Assert.AreEqual(1, yDir);
			Change();
			Assert.AreEqual(-1, xDir);
			Assert.AreEqual(0, yDir);
			Change();
			Assert.AreEqual(0, xDir);
			Assert.AreEqual(-1, yDir);
			Change();
			Assert.AreEqual(1, xDir);
			Assert.AreEqual(0, yDir);
		}

